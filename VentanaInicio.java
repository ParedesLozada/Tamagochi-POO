    public void setearTabla(){
        this.tblModel = new DefaultTableModel();
        this.tblModel.addColumn("id");
        this.tblModel.addColumn("nombre");
        this.tblModel.addColumn("vida");
        this.tblModel.addColumn("energ√≠a");
        this.tblModel.addColumn("felicidad");
        this.tblModel.addColumn("estado");
        
        for (Tamagochi tamagochi : tamagochis) {
            tblModel.addRow(new Object []{tamagochi.getId(), tamagochi.getNombre(), 
                tamagochi.getVida(), tamagochi.getEnergia(), tamagochi.getFelicidad(), tamagochi.getEstado()});
        }
        
        this.jTblTamagochis.setModel(this.tblModel);
    }
    
    public Tamagochi selectTamagochi(){
        this.ID = this.jTblTamagochis.getSelectedRow();
        Tamagochi selectedTamagochi = this.tamagochis.get(this.ID);
        return selectedTamagochi;
    }
    
    
